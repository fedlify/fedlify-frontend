import React from "react";
import { motion, useAnimation, SVGMotionProps } from "framer-motion";
import { useEffect } from "react";

// Type for Path props
type PathProps = SVGMotionProps<SVGPathElement> & {
    fill: string;
    delay?: number;
    duration?: number;
    animated?: boolean;
};

// Path: Animates SVG path drawing and fill using Framer Motion
const Path: React.FC<PathProps> = ({
    fill,
    delay = 0,
    duration = 0.5,
    animated = true,
    ...props
}) => {
    const controls = useAnimation();


    useEffect(() => {
        if (animated) {
            // Animate the path drawing and fill opacity
            controls
                .start({
                    pathLength: 1,
                    fillOpacity: 1,
                    transition: {
                        pathLength: { duration, delay, ease: "easeInOut" },
                        fillOpacity: { delay: delay + duration, duration },
                    },
                })
                .then(() => {
                    // After fill appears, remove stroke for a clean look
                    controls.start({
                        pathLength: 0,
                        transition: { duration },
                    });
                });
        }
    }, [controls, delay, duration, animated]);

    return (
        <motion.path
            fill={String(fill)}
            {...(animated && {
                stroke: fill,
                strokeWidth: 0.5,
                initial: { pathLength: 0, fillOpacity: 0 },
                animate: controls,
            })}
            {...props}
        />
    );
};

